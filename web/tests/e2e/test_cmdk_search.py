@pytest.mark.e2e
def test_plausible_events_fired(page, server):
    """Verify the three tracked event types fire."""
    page.goto(server)
    page.keyboard.press("Meta+k")
    page.locator('[data-testid="cmdk-input"]').wait_for(state="visible")
    page.locator('[data-testid="cmdk-input"]').fill("aapl")
    page.wait_for_timeout(200)
    page.keyboard.press("Enter")
    page.wait_for_function("() => window.location.hash.startsWith('#go=')", timeout=2000)

    events = page.evaluate("() => window.__cmdk_events || []")
    names = {e["event"] for e in events}
    assert "search_opened" in names, "missing search_opened event"
    assert "search_query" in names, "missing search_query event"
    assert "search_result_click" in names, "missing search_result_click event"
