@pytest.mark.e2e
def test_cmdk_opens_palette(page, server):
    page.goto(server)
    # No dialog visible initially.
    assert page.locator('[data-testid="cmdk-dialog"]').count() == 0
    # Cmd+K opens it (Playwright maps Meta to Cmd on Mac, Ctrl on Linux).
