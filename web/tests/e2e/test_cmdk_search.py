_BUILD_SCRIPT = _REPO / "scripts" / "build_search_index.py"


# A self-contained HTML shell that mimics CommandPaletteProvider + CommandPalette.
# Keep this in sync with the React component's testable surface:
#   - data-testid="cmdk-dialog" appears when open
#   - data-testid="cmdk-input"  is the search input
#   - data-testid="cmdk-result-<type>" is each result row
#   - Cmd+K / Ctrl+K toggle
#   - `/` opens IFF not focused inside <input>
#   - Esc closes
#   - Arrow keys navigate, Enter activates
#   - Recent searches via localStorage key "phase:cmdk:recent"
_INDEX_HTML = r"""<!DOCTYPE html>
<html lang="zh-CN">
<head>
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width,initial-scale=1">
  <title>CmdK harness</title>
  <style>
    body { font: 14px system-ui; margin: 0; padding: 24px; }
    .cmdk-dialog { position: fixed; inset: 0; background: rgba(0,0,0,.4); display:flex; justify-content:center; align-items:flex-start; }
    .cmdk-panel { background: white; width: 600px; max-width: 100vw; margin-top: 10vh; border-radius: 8px; overflow: hidden; }
    .cmdk-panel input { width: 100%; padding: 12px 16px; border: 0; outline: none; font-size: 16px; }
    .cmdk-list { max-height: 60vh; overflow-y: auto; }
    .cmdk-row { padding: 10px 16px; cursor: pointer; }
    .cmdk-row[aria-selected="true"] { background: #f3f4f6; }
    .cmdk-row .title { font-weight: 500; }
    .cmdk-row .sub { color: #6b7280; font-size: 12px; }
    .cmdk-group { padding: 4px 16px; text-transform: uppercase; color: #9ca3af; font-size: 11px; }
    @media (max-width: 640px) {
      .cmdk-panel { width: 100vw; max-width: none; height: 100dvh; margin-top: 0; border-radius: 0; }
    }
  </style>
</head>
<body>
  <h1 data-testid="home-heading">Harness page</h1>
  <input type="text" data-testid="other-input" placeholder="something else">
  <textarea data-testid="other-textarea"></textarea>

  <div id="cmdk-root"></div>

  <script>
  (function() {
    let isOpen = false;
    let query = "";
    let hits = [];
    let activeIdx = 0;
    let index = null;
    let lastSource = "shortcut";

    const RECENT_KEY = "phase:cmdk:recent";

    function norm(s) { return s.toLowerCase().trim().replace(/\s+/g, " "); }

    function isSubsequence(needle, haystack) {
      if (!needle.length) return true;
      let i = 0;
      for (let j = 0; j < haystack.length && i < needle.length; j++) {
        if (haystack[j] === needle[i]) i++;
      }
      return i === needle.length;
    }

    function daysSince(iso) {
      if (!iso) return Infinity;
      const t = Date.parse(iso);
      if (Number.isNaN(t)) return Infinity;
      return (Date.now() - t) / 86400000;
    }

    function scoreEntry(entry, q) {
      if (!q) return 0;
      const title = norm(entry.title);
      let best = 0;
      if (title === q) best = Math.max(best, 1000);
      else if (title.startsWith(q)) best = Math.max(best, 700);
      else if (title.includes(q)) best = Math.max(best, 300);
      for (const kw of entry.keywords || []) {
        if (kw === q) { best = Math.max(best, 1000); break; }
        else if (kw.startsWith(q)) best = Math.max(best, 500);
        else if (kw.includes(q)) best = Math.max(best, 200);
      }
      if (best === 0 && q.length >= 2 && isSubsequence(q, title)) best = 100;
      if (best === 0) return 0;
      const w = (entry.weight || 0) * 50;
      let rec = 0;
      if ((entry.type === "paper" || entry.type === "newsletter")) {
        const d = daysSince(entry.date);
        if (Number.isFinite(d)) rec = Math.max(0, 20 - d / 18);
      }
      return best + w + rec;
    }

    function searchIndex(idx, q, lim) {
      lim = lim || 8;
      const nq = norm(q);
      if (!nq) return [];
      const out = [];
      for (const e of idx) {
        const s = scoreEntry(e, nq);
        if (s > 0) out.push(Object.assign({}, e, { score: s }));
      }
      out.sort((a, b) => b.score - a.score);
      return out.slice(0, lim);
    }

    async function ensureIndex() {
      if (index) return index;
      const r = await fetch("/search-index.json");
      index = await r.json();
      return index;
    }

    function loadRecent() {
      try { return JSON.parse(localStorage.getItem(RECENT_KEY) || "[]"); } catch { return []; }
    }
    function pushRecent(q) {
      const t = (q || "").trim();
      if (!t) return;
      const prev = loadRecent();
      const next = [t].concat(prev.filter(p => p !== t)).slice(0, 5);
      localStorage.setItem(RECENT_KEY, JSON.stringify(next));
    }

    function open_(source) {
      isOpen = true;
      lastSource = source || "shortcut";
      ensureIndex().then(render).catch(render);
      // emit a tracked event marker for tests
      window.__cmdk_events = window.__cmdk_events || [];
      window.__cmdk_events.push({ event: "search_opened", source: lastSource });
      render();
    }
    function close_() {
      isOpen = false;
      query = "";
      activeIdx = 0;
      hits = [];
      render();
    }

    function go(hit, pos) {
      window.__cmdk_events = window.__cmdk_events || [];
      window.__cmdk_events.push({
        event: "search_result_click",
        result_type: hit.type,
        result_position: pos,
      });
      pushRecent(query || hit.title);
      // simulate router.push by replacing URL hash for testability.
      window.location.hash = "go=" + encodeURIComponent(hit.url);
      close_();
    }

    function render() {
      const root = document.getElementById("cmdk-root");
      if (!isOpen) { root.innerHTML = ""; return; }
      hits = searchIndex(index || [], query, 8);
      if (activeIdx >= hits.length) activeIdx = Math.max(0, hits.length - 1);

      // group order
      const groupOrder = ["company", "universality_class", "paper", "newsletter", "docs"];
      const groupLabels = {
        company: "公司", universality_class: "普适类",
        paper: "论文", newsletter: "周刊", docs: "文档",
      };
      const grouped = {};
      for (const t of groupOrder) grouped[t] = [];
      hits.forEach(h => { (grouped[h.type] = grouped[h.type] || []).push(h); });

      let pos = -1;
      let html = '';
      for (const t of groupOrder) {
        if (!grouped[t] || !grouped[t].length) continue;
        html += '<div class="cmdk-group">' + groupLabels[t] + '</div>';
        for (const h of grouped[t]) {
          pos += 1;
          const sel = (pos === activeIdx);
          html += '<div class="cmdk-row" role="option" '
               + 'aria-selected="' + sel + '" '
               + 'data-cmdk-pos="' + pos + '" '
               + 'data-cmdk-url="' + h.url + '" '
               + 'data-testid="cmdk-result-' + h.type + '">'
               + '<div class="title">' + h.title + '</div>'
               + '<div class="sub">' + h.subtitle + '</div>'
               + '</div>';
        }
      }
      if (!hits.length && query.trim()) html = '<div style="padding:24px;color:#6b7280">没有匹配 “' + query + '” 的结果</div>';
      if (!query.trim()) {
        const recent = loadRecent();
        html = '';
        if (recent.length) {
          html += '<div class="cmdk-group">最近搜索</div>';
          recent.forEach(r => {
            html += '<div class="cmdk-row" data-cmdk-recent="' + r + '">' + r + '</div>';
          });
        }
        html += '<div class="cmdk-group">热门</div>'
              + '<div class="cmdk-row" data-cmdk-trending="AAPL">AAPL</div>'
              + '<div class="cmdk-row" data-cmdk-trending="scheffer">Scheffer fold</div>';
      }

      root.innerHTML =
        '<div class="cmdk-dialog" role="dialog" aria-modal="true" data-testid="cmdk-dialog">'
        + '<div class="cmdk-panel">'
        + '<input type="text" data-testid="cmdk-input" role="combobox" '
        + ' aria-controls="cmdk-listbox" placeholder="搜公司、普适类…" autofocus>'
        + '<div class="cmdk-list" role="listbox" id="cmdk-listbox" data-testid="cmdk-list">' + html + '</div>'
        + '</div></div>';

      const input = root.querySelector('[data-testid="cmdk-input"]');
      input.value = query;
      setTimeout(() => input.focus(), 0);

      input.addEventListener("input", e => {
        query = e.target.value;
        window.__cmdk_events = window.__cmdk_events || [];
        window.__cmdk_events.push({
          event: "search_query",
          query_length: query.length,
          result_count: searchIndex(index || [], query, 8).length,
        });
        activeIdx = 0;
        render();
      });

      root.querySelectorAll('[data-cmdk-recent]').forEach(el => {
        el.addEventListener("click", () => { query = el.getAttribute("data-cmdk-recent"); render(); });
      });
      root.querySelectorAll('[data-cmdk-trending]').forEach(el => {
        el.addEventListener("click", () => { query = el.getAttribute("data-cmdk-trending"); render(); });
      });
      root.querySelectorAll('.cmdk-row[role="option"]').forEach(el => {
        el.addEventListener("click", () => {
          const pos = parseInt(el.getAttribute("data-cmdk-pos"), 10);
          go(hits[pos], pos);
        });
      });
    }

    function isEditingTarget(t) {
      if (!t || !t.tagName) return false;
      const tag = t.tagName;
      if (tag === "INPUT" || tag === "TEXTAREA" || tag === "SELECT") return true;
      if (t.isContentEditable) return true;
      return false;
    }

    document.addEventListener("keydown", (e) => {
      // Cmd+K / Ctrl+K — toggle (works even inside other inputs)
      if ((e.metaKey || e.ctrlKey) && (e.key === "k" || e.key === "K")) {
        e.preventDefault();
        isOpen ? close_() : open_("shortcut");
        return;
      }
      // Bare `/` — only when not inside a text field
      if (e.key === "/" && !isEditingTarget(e.target) && !e.metaKey && !e.ctrlKey && !e.altKey) {
        e.preventDefault();
        isOpen ? close_() : open_("shortcut");
        return;
      }
      if (!isOpen) return;
      if (e.key === "Escape") { e.preventDefault(); close_(); return; }
      if (e.key === "ArrowDown") {
        e.preventDefault();
        activeIdx = Math.min(activeIdx + 1, Math.max(0, hits.length - 1));
        render();
        return;
      }
      if (e.key === "ArrowUp") {
        e.preventDefault();
        activeIdx = Math.max(0, activeIdx - 1);
        render();
        return;
      }
      if (e.key === "Enter") {
        e.preventDefault();
        const t = hits[activeIdx];
        if (t) go(t, activeIdx);
      }
    });

    // Expose API for /search-style deep-link test:
    window.__cmdk_open = open_;
    window.__cmdk_close = close_;
  })();
  </script>
</body>
</html>
"""


class _SilentHandler(http.server.SimpleHTTPRequestHandler):
    def log_message(self, *_a, **_kw):
        return

    def do_GET(self):  # noqa: N802
        path = self.path.split("?", 1)[0]
        if path in ("/", "/index.html", "/search"):
            self._send(200, "text/html; charset=utf-8", _INDEX_HTML.encode("utf-8"))
            return
        if path == "/search-index.json" and _INDEX_FILE.is_file():
            self._send(200, "application/json", _INDEX_FILE.read_bytes())
            return
        self._send(404, "text/plain", b"not found")

    def _send(self, status, ctype, body):
        self.send_response(status)
        self.send_header("Content-Type", ctype)
        self.send_header("Content-Length", str(len(body)))
        self.end_headers()
        self.wfile.write(body)


@pytest.fixture(scope="module")
def index_built():
    """Ensure search-index.json exists for the harness fetch()."""
    if not _INDEX_FILE.is_file():
        subprocess.run(
            [sys.executable, str(_BUILD_SCRIPT)],
            cwd=_REPO,
            check=True,
            capture_output=True,
        )
    assert _INDEX_FILE.is_file(), "search-index.json should exist after build"
    return _INDEX_FILE


@pytest.fixture(scope="module")
def server(index_built):
    """Boot tiny HTTP server on an ephemeral port for the module."""
    httpd = socketserver.TCPServer(("127.0.0.1", 0), _SilentHandler)
    port = httpd.server_address[1]
    th = threading.Thread(target=httpd.serve_forever, daemon=True)
    th.start()
    yield f"http://127.0.0.1:{port}"
    httpd.shutdown()
    httpd.server_close()


