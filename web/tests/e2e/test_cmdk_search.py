"""W13-E (session #10) Cmd+K palette e2e — site-wide search.

These tests cannot easily boot a real Next.js server in CI, so we mirror
the W12-E PWA test pattern: stand up a tiny `http.server` that serves a
self-contained HTML shell which embeds a minimal-but-faithful JS
implementation of the palette behavior, plus the real `search-index.json`
generated by `scripts/build_search_index.py`.

That lets us assert:
  • Cmd+K opens the palette
  • Ctrl+K opens the palette (cross-platform)
  • Typing "appl" surfaces AAPL as the top result
  • Arrow-Down + Enter navigates to /company/AAPL
  • Esc closes the palette
  • Keyboard shortcut does NOT trigger inside an <input> for bare `/`
  • Cmd+K still works inside an <input> (modifier disambiguates)
  • Mobile viewport renders full-screen
  • Recent searches persist across opens (localStorage)
"""
